package com.java.basics;

public class DataTypes {
	
	public static void main(String[] args) {
		// Data Types
		// Strict type
		// 1.Primitive Data Types - Do not need Object
		// 1.Boolean Type :boolean(true/false)
		// 2.Numeric Type :
		// 1.Character Type :char
		// 2.Integral Type
		// Integer :byte,int,short,long
		// Floating point :float ,double
		// 2.Non-Primitive Data types - Need to create Object
		// String,Array,Class

		// 1.byte
		// size -->1 byte-->8 bits
		//
		// range --> -128 - 127
		byte b = 20;
		byte b1 = -50;
		// byte b2 =130;
		System.out.println(b);
		// 2.short
		// size - 2 Bytes -16 bits
		// range --> -32768 --32767
		short s1 = 1234;
		System.out.println(s1);

		// 3 .int
		// size - 4 bytes -->32 bits
		// range --> -2147483648 --> 2147483647
		int a = 10;
		System.out.println(a);

		// 4 .long
		// size - 8 bytes-->64 bits
		long l = 9038168803L;
		System.out.println(l);

		// 5 float
		// size - 4 bytes
		// upto 7 digit decimal
		float f = 12.33f;
		System.out.println(f);
		// double
		// size -->8 bytes
		double d = 12.33;
		System.out.println(d);
		// upto 16 digit decimal

// char
		char c1 = 'q';
		char c = '1';
		char c2 = '#';
		System.out.println(c2);
// boolean		
		boolean bl = true;
		System.out.println(bl);

	}
}